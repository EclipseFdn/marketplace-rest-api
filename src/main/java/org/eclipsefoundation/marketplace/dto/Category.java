package org.eclipsefoundation.marketplace.dto;

import java.util.ArrayList;
import java.util.List;

import io.quarkus.runtime.annotations.RegisterForReflection;

/**
 * Represents a listing category in the marketplace
 * 
 * @author Martin Lowe
 *
 */
@RegisterForReflection
public class Category {
	private Integer id;
	private String name;
	private String url;
	private List<Integer> marketIds;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the marketIds
	 */
	public List<Integer> getMarketIds() {
		return new ArrayList<>(marketIds);
	}

	/**
	 * @param marketIds the marketIds to set
	 */
	public void setMarketIds(List<Integer> marketIds) {
		this.marketIds = new ArrayList<>(marketIds);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((marketIds == null) ? 0 : marketIds.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		result = prime * result + ((url == null) ? 0 : url.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Category other = (Category) obj;
		if (id != other.id)
			return false;
		if (marketIds == null) {
			if (other.marketIds != null)
				return false;
		} else if (!marketIds.equals(other.marketIds))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		if (url == null) {
			if (other.url != null)
				return false;
		} else if (!url.equals(other.url))
			return false;
		return true;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("Category [id=");
		builder.append(id);
		builder.append(", name=");
		builder.append(name);
		builder.append(", url=");
		builder.append(url);
		builder.append(", marketIds=");
		builder.append(marketIds);
		builder.append("]");
		return builder.toString();
	}
}
