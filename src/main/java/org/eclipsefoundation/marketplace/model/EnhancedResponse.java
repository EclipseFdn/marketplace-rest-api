package org.eclipsefoundation.marketplace.model;

/**
 * Used in responses to requests to include extra contextual data
 * 
 * @author Martin Lowe
 *
 */
public class EnhancedResponse {
	private int size;
	private Object data;
	
	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
	/**
	 * @return the data
	 */
	public Object getData() {
		return data;
	}
	/**
	 * @param data the data to set
	 */
	public void setData(Object data) {
		this.data = data;
	}
	
}
